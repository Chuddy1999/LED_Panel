vector<vector<byte>>grid_for_content(byte pos){
    vector<vector<byte>> grid_array(5, vector<byte>(3));

    for (int h = 0; h < 5; h++){

        for (int w = 0; w < 3; w++){
            grid_array[h][w] = grid[h+1][w+pos];
        }
    }
    return grid_array;
}

